function Bezier(P0, P1, P2, P3)

x0 = P0(1); y0 = P0(2);
x1 = P1(1); y1 = P1(2);
x2 = P2(1); y2 = P2(2);
x3 = P3(1); y3 = P3(2);

t = 0:0.001:1;
x = x0*(1-t).^3 + 3*x1*t.*(1-t).^2 + 3*x2*t.*(1-t) + x3*t.^3;
y = y0*(1-t).^3 + 3*y1*t.*(1-t).^2 + 3*y2*t.*(1-t) + y3*t.^3;
plot(x, y, 'g');
hold on

plot([P0(1), P1(1)], [P0(2), P1(2)]);
plot([P2(1), P1(1)], [P2(2), P1(2)]);
plot([P2(1), P3(1)], [P2(2), P3(2)]);
hold off

end